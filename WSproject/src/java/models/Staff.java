package models;

/**
 *
 * @author Admin
 */
public class Staff {
    int id;
    String name;
    String phone;
    String address;
    String imglink;

    public Staff(int id, String name, String phone, String address, String imglink) {
        this.id = id;
        this.name = name;
        this.phone = phone;
        this.address = address;
        this.imglink = imglink;
    }

    public Staff(String name) {
        this.name = name;
    }

    public String getImglink() {
        return imglink;
    }

    public void setImglink(String imglink) {
        this.imglink = imglink;
    }

    public Staff(int id, String name, String phone, String address) {
        this.id = id;
        this.name = name;
        this.phone = phone;
        this.address = address;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }
    
}
